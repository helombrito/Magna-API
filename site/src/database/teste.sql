CREATE DATABASE Magna;
USE Magna;

CREATE TABLE usuario(
	idUsuario INT PRIMARY KEY AUTO_INCREMENT, -- IDENTITY(1,1)
	nome VARCHAR(100),
    email VARCHAR(100) NOT NULL UNIQUE,
    senha VARCHAR(60) NOT NULL,
    cpf CHAR(11) UNIQUE,
    dtNasc DATE,
    createdAt DATETIME DEFAULT CURRENT_TIMESTAMP
